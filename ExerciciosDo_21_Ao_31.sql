ARTISTS.NAME  
FROM ALBUMS
JOIN SONGS ON SONGS.ALBUM_ID = ALBUMS.ALBUMS_ID
JOIN ARTISTS ON ALBUMS.ARTIST_ID = ARTISTS.ARTISTS_ID;

