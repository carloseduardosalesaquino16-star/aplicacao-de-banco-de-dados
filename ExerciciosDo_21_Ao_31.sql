# Exercicio 30
SELECT SONGS.TITLE, SONGS.DURATION_SECONDS ,SONGS.SONGS_ID,
SONG_REVIEWS.RATING
FROM SONGS
 LEFT JOIN SONG_REVIEWS ON SONG_REVIEWS.SONG_REVIEWS_ID = SONGS.SONGS_ID
 WHERE DURATION_SECONDS > 200 OR SONG_REVIEWS.RATING < 3; 
 
